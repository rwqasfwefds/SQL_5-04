-- 연습문제 : 제약조건
-- 1) EMPLOYEE 테이블의 구조를 복사하여 EMP_SAMPLE란 이름의 테이블을 만드셈
-- 사원 테이블의 사원번호 컬럼(ENO)에 테이블 레벨로 PTIMARY KEY 제약조건을 지정하되,
-- 제약 조건 이름은 PK_MY_EMP로 하셈
CREATE TABLE EMP_SAMPLE
AS
SELECT *
FROM EMPLOYEE
WHERE 1 = 2;
-- 기본키(PRIMARY KEY)
ALTER TABLE EMP_SAMPLE
ADD CONSTRAINT PK_MY_EMP PRIMARY KEY(ENO);

-- 제약조건 걸렸는지 확인
SELECT * FROM USER_CONSTRAINTS
WHERE TABLE_NAME = 'EMP_SAMPLE';

-- 2) DEPARTMENT 테이블의 구조를 복사하여 DEPT_SAMPLE란 이름의 테이블을 만드셈
-- 부서번호 컬럼(DNO)에 PRIMARY KEY 제약조건을 지정하되,
-- 제약 조건 이름은 PK_MY_DEPT로 하셈
CREATE TABLE DEPT_SAMPLE
AS
SELECT *
FROM DEPARTMENT
WHERE 1 = 2;

ALTER TABLE DEPT_SAMPLE
ADD CONSTRAINT PK_MY_DEPT PRIMARY KEY(DNO);

-- 제약조건 걸렸는지 확인
SELECT * FROM USER_CONSTRAINTS
WHERE TABLE_NAME = 'DEPT_SAMPLE';

-- 3) 사원 테이블의 부서번호 컬럼에 존재하지 않는 부서의 사원이 배정되지 않도록
-- 외래 키(FOREIGN KEY : FK)제약조건을 지정하되, FK_MY_DEPT_EMP로 하셈
-- 참조 테이블 : DEPARTMENT
-- 참조키, 외래키(FOREIGN KEY : FK)
ALTER TABLE EMP_SAMPLE
ADD CONSTRAINT FK_MY_DEPT_EMP
FOREIGN KEY(DNO) REFERENCES DEPARTMENT(DNO);

-- 제약조건 걸렸는지 확인
SELECT * FROM USER_CONSTRAINTS
WHERE TABLE_NAME = 'EMP_SAMPLE';
